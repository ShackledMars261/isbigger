def isBigger(int1: int, int2: int) -> bool:
    """
    Returns True if big is greater than small, False otherwise
    :param int1: int
    :param int2: int
    :return: True if int1 > int2, False otherwise
    """
    return int1 > int2

def isSmaller(int1: int, int2: int) -> bool:
    """
    Returns True if small is less than big, False otherwise
    :param int1: int
    :param int2: int
    :return: True if int1 < int2, False otherwise
    """
    return int1 < int2

def isEqual(int1: int, int2: int) -> bool:
    """
    Returns True if int1 is equal to int2, False otherwise
    :param int1: int
    :param int2: int
    :return: True if int1 == int2, False otherwise
    """
    return int1 == int2